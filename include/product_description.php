<h3>What is it?</h3>
                <p>This is a tool to generate a Proof-of-Existence of a file or record on Blockchain. By submiting this form you will be uploading the file's signature with it's associated information into the Private Decentralized Public Ledger. Records stored here can later be fetched to prove that this file existed on particular date &amp; time with the associated information.</p>
                <p>Computationally & Technologically it is impossible to fake the Document's signature and/or modify the past records. Hence this PoE can be used to prove the file's existence in legal matters.</p>
                <h3>How it works?</h3>
                <ul>
                    <li><b>Step 1:</b> Fill the form & Upload the file</li>
                    <li><b>Step 2:</b> Copy the link & Share it with people you want to prove its existence</li>
                    <li><b>Step 3:</b> Anyone with link or file can verify the existence of the file at particular date & time.</li>
                </ul>

